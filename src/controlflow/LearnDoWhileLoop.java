package controlflow;

public class LearnDoWhileLoop {
    public static void main(String args[] ){
        int number=10;
        do{
            System.out.println("Learning do While loop with increment pattern");
            number++;
        }
        while(number<15);
        System.out.println("*************************************");
        int num=20;
        do{
            System.out.println("Learning do while loop with decrement manner");
            num--;
        }
        while(num>15);
    }
}
